package lesson7;

public class App_170 {
    public static void main(String[] args) {
        int n = 1022;
        double x = 0;
        int a = (int) Math.sqrt(n) + 1;
        for (int i = a; i <= a; i ++) {
            if (Math.pow(2, i) > n) {
                x = Math.pow(2,1);
                break;
            }
        }
        System.out.println(x);

    }
}
