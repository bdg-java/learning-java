public class App_271 {
    public static void main(String[] args) {

        int x = 0;
        char[] a = { 'a', '4', 'e', 'd', 'z', 'a' };

        for (int i = 0; i < a.length; i++) {
            if (a[i] == 'a')
                x++;
        }
        System.out.println(x);
        }
}
