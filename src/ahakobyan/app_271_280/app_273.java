package ahakobyan.app_271_280;

public class app_273 {
    public static void main(String[] args) {

        char[] x = {'e', 'b', 'c', '4', 'f', 'd', 'e' };
        int a = 0;

        for (int i = 0; i < x.length; i ++) {
            if (x[i] == 's' ) {
                a = a + x[i];
            }
        }
        System.out.println(a);
    }
}
